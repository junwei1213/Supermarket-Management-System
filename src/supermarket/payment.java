    private void jComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxActionPerformed
        // TODO add your handling code here:
        try {
             String fetch_row="select*from product where Product_id=? ";
             PreparedStatement Statement = conn.prepareStatement(fetch_row);
             Statement.setString(1,(String)jComboBox.getSelectedItem());
             ResultSet set = Statement.executeQuery();
             while(set.next())
             {
                 jTextFieldName.setText(set.getString("Product_Name"));
                 jTextFieldQuan.setText(set.getString("Product_Stock"));
                 jTextFieldPrice.setText(set.getString("Price"));
             }
        }
        catch(Exception e)
        {
            
        }
    }//GEN-LAST:event_jComboBoxActionPerformed

